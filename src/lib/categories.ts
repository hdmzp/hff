export const OTHER_CATEGORY = "기타";

export const ALL_CATEGORIES = [...CATEGORY_RULES.map(([c]) => c), OTHER_CATEGORY];

/** 기능성 텍스트(들)를 카테고리 목록으로 변환 (다중 라벨, 매칭 없으면 ["기타"]) */
export function categorize(...texts: (string | undefined)[]): string[] {
  const joined = texts.filter(Boolean).join(" ");
  if (!joined.trim()) return [OTHER_CATEGORY];
  const matched: string[] = [];
  for (const [category, keywords] of CATEGORY_RULES) {
    if (keywords.some((kw) => joined.includes(kw))) matched.push(category);
  }
  return matched.length > 0 ? matched : [OTHER_CATEGORY];
}
